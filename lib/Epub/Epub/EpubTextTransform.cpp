const CharMapping* findCharMapping(uint32_t cp) {
  for (const auto& mapping : kCharMappings) {
    if (mapping.simplified == cp) {
      return &mapping;
    }
  }
  return nullptr;
}

const PhraseMapping* findPhraseMapping(const char* cursor, const char* end) {
  for (const auto& mapping : kPhraseMappings) {
    const size_t len = strlen(mapping.simplified);
    if (cursor + len <= end && strncmp(cursor, mapping.simplified, len) == 0) {
      return &mapping;
    }
  }
  return nullptr;
}
