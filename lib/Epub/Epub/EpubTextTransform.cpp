std::string simplifiedToTraditional(const char* text, const int len) {
  if (!text || len <= 0) {
    return "";
  }

  std::string converted;
  converted.reserve(static_cast<size_t>(len) + 8);
  const auto* cursor = reinterpret_cast<const unsigned char*>(text);
  const auto* end = reinterpret_cast<const unsigned char*>(text + len);

  while (cursor < end && *cursor != 0) {
    if (const auto* phrase = findPhraseMapping(reinterpret_cast<const char*>(cursor), reinterpret_cast<const char*>(end))) {
      converted += phrase->traditional;
      cursor += strlen(phrase->simplified);
      continue;
    }

    const auto* before = cursor;
    const uint32_t cp = utf8NextCodepoint(&cursor);
    if (cursor > end) {
      converted.append(reinterpret_cast<const char*>(before), static_cast<size_t>(end - before));
      break;
    }
    if (const auto* mapping = findCharMapping(cp)) {
      converted += mapping->traditional;
    } else {
      converted.append(reinterpret_cast<const char*>(before), static_cast<size_t>(cursor - before));
    }
  }
  return converted;
}

std::string simplifiedToTraditional(const std::string& text) {
  return simplifiedToTraditional(text.c_str(), static_cast<int>(text.size()));
}

